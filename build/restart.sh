#!/bin/bash

# restart compose with the latest images
docker run --rm \
    -v /var/run/docker.sock:/var/run/docker.sock \
    -v "$PWD:$PWD" \
    -w="$PWD" \
    docker/compose:1.24.0 down

docker rmi $(docker images -f "dangling=true" -q)

sh ${0%/*}/pull-images.sh

docker run --rm \
    -v /var/run/docker.sock:/var/run/docker.sock \
    -v "$PWD:$PWD" \
    -w="$PWD" \
    docker/compose:1.24.0 up -d --force-recreate